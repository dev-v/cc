package com.cc.report.xml;
/**
 * 一个标签值域
 * @author cwl
 * @version 1.0
 */
public class Label {
	private String value;
	private String text;
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
}
